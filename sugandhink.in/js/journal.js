function getArticleParam() {
    const params = new URLSearchParams(window.location.search);
    return params.get('article');
}

function renderJournal() {
    const container = document.getElementById('journal-content');
    if (!container) return;

    const articleId = getArticleParam();
    const article = articleId ? journalArticles.find(a => a.id === articleId) : null;

    if (article) {
        const related = journalArticles.filter(a => a.id !== article.id).slice(0, 2);
        let relatedHtml = '';
        if (related.length > 0) {
            relatedHtml = '<div class="journal-related"><h3>Further Reading</h3><div class="jr-grid">';
            related.forEach(r => {
                relatedHtml += `<a href="?article=${r.id}" class="jr-item">
                    <div class="jr-thumb"><img src="${r.image}" alt="${escJournal(r.title)}" loading="lazy"></div>
                    <div class="jr-info">
                        <span class="jr-date">${r.date}</span>
                        <span class="jr-title">${escJournal(r.title)}</span>
                    </div>
                </a>`;
            });
            relatedHtml += '</div></div>';
        }

        container.innerHTML = `
            <div class="journal-article-view">
                <button class="journal-back-btn" id="journal-back" onclick="history.pushState({}, '', window.location.pathname); window.dispatchEvent(new CustomEvent('journal:back'))">
                    <svg width="14" height="14" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round"><path d="M19 12H5"/><path d="M12 19l-7-7 7-7"/></svg>
                    Back to Journal
                </button>
                <div class="journal-article-hero">
                    <div class="jar-image"><img src="${article.image}" alt="${escJournal(article.imageAlt)}"></div>
                    <div class="jar-meta">
                        <span class="jar-date">${article.date}</span>
                        <h1>${escJournal(article.title)}</h1>
                    </div>
                </div>
                <div class="journal-article-body">
                    ${article.content}
                </div>
                ${relatedHtml}
            </div>
        `;

        document.getElementById('journal-back')?.addEventListener('click', () => {
            history.pushState({}, '', window.location.pathname);
            window.dispatchEvent(new CustomEvent('journal:back'));
        });

        document.title = `${article.title} - Sugandh Ink`;
    } else {
        let html = `
            <div class="journal-hero" data-reveal>
                <p class="journal-overline">The Sugandh Ink Atelier</p>
                <h1>The <em>Journal</em></h1>
                <p class="journal-lead">Notes on olfaction, tradition, and the craft of perfumery. A record of the Maison's ongoing enquiry into the silent art of scent.</p>
            </div>
            <div class="journal-grid" data-reveal>
        `;

        journalArticles.forEach((a, idx) => {
            html += `
                <a href="?article=${a.id}" class="journal-card" data-reveal>
                    <div class="jc-image">
                        <img src="${a.image}" alt="${escJournal(a.imageAlt)}" loading="lazy">
                    </div>
                    <div class="jc-body">
                        <span class="jc-date">${a.date}</span>
                        <h3>${escJournal(a.title)}</h3>
                        <p>${escJournal(a.excerpt)}</p>
                        <span class="jc-read">Read Article</span>
                    </div>
                </a>
            `;
        });

        html += '</div>';
        container.innerHTML = html;
        document.title = 'The Journal - Sugandh Ink';
    }
}

function escJournal(str) {
    if (!str) return '';
    const div = document.createElement('div');
    div.textContent = str;
    return div.innerHTML;
}

function initJournal() {
    renderJournal();

    window.addEventListener('journal:back', renderJournal);

    window.addEventListener('popstate', renderJournal);

    document.addEventListener('click', (e) => {
        const card = e.target.closest('.journal-card');
        if (card) return;
    });
}

document.addEventListener('DOMContentLoaded', initJournal);
